// 📌 Introdução ao JavaScript

// JavaScript é uma linguagem de programação usada principalmente para
// tornar páginas web interativas e dinâmicas.

// Enquanto o HTML estrutura o conteúdo e o CSS cuida da aparência,
// o JavaScript adiciona comportamento à página.

// 💡 O que o JavaScript faz?

// Com JavaScript, é possível:

// Criar interações com o usuário (cliques, digitação, etc.)
// Validar formulários
// Alterar conteúdos da página em tempo real
// Criar animações
// Consumir APIs (buscar dados de servidores)
// Desenvolver aplicações completas (front-end e back-end)
// 🌐 Onde o JavaScript roda?
// No navegador (Chrome, Edge, etc.)
// No servidor (usando Node.js)
// ⚙️ Como o JavaScript funciona?

// O JavaScript manipula elementos da página através do DOM (Document Object Model),
//  permitindo alterar textos, estilos e estruturas HTML dinamicamente.

// 🧠 Principais conceitos iniciais
// Variáveis → armazenam valores
// Tipos de dados → string, number, boolean, etc.
// Condições → if / else
// Funções → blocos de código reutilizáveis
// Eventos → ações do usuário (click, input, etc.)
// 🔥 Exemplo simples:

        let nome = "Jayro";

